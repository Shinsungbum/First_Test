package hrd;

public class HrdVO {
	private String job_title, job_overview, job_perform, job_info, writer;
	private int id;
	
	
	public String getJob_title() {
		return job_title;
	}
	public void setJob_title(String job_title) {
		this.job_title = job_title;
	}
	public String getJob_overview() {
		return job_overview;
	}
	public void setJob_overview(String job_overview) {
		this.job_overview = job_overview;
	}
	public String getJob_perform() {
		return job_perform;
	}
	public void setJob_perform(String job_perform) {
		this.job_perform = job_perform;
	}
	public String getJob_info() {
		return job_info;
	}
	public void setJob_info(String job_info) {
		this.job_info = job_info;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
}
